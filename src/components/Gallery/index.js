class Gallery extends Component {
  state = {activeId: 0}

  onClickImg = id => {
    this.setState({activeId: id})
  }

  filter = () => {
    const {activeId} = this.state
    const filtered = imagesList.filter(each => each.id === activeId)
    return filtered
  }

  render() {
    const {activeId} = this.state
    const selectedImg = this.filter()[0]

    return (
      <div>
        <img
          className="main"
          src={selectedImg.imageUrl}
          alt={selectedImg.imageAltText}
        />
        <h1>Nature Photography</h1>
        <p>Nature Photography by Rahul </p>
        <div>
          <ul className="img-container">
            {imagesList.map(item => {
              const select = item.id === activeId ? 'selected' : 'unselected'

              return (
                <Thumbnail
                  key={item.id}
                  onClickImg={this.onClickImg}
                  imagesList={item}
                  select={select}
                />
              )
            })}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
